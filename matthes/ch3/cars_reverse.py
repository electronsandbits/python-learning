# This program demonstrates how to print a list in reverse order.
# with the reverse() method. The reverse() method changes the order
# of a list permanently, but you can revert to the original order
# anytime by applying reverse() to the same list a second time.

cars= ['bmw', 'audi', 'toyota', 'suburu']
print(cars)

cars.reverse()
print(cars)
