# This program demonstrates how to sort a list temporarily
# with the sorted() method.The sorted() method maintain the
# original order of a list but present it in a sorted order.

cars= ['bmw', 'audi', 'toyota', 'suburu']

print("Here is the original list:")
print(cars)

print("\nHere is the sorted list:")
print(sorted(cars))

print("\nHere is the original list again:")
print(cars)
